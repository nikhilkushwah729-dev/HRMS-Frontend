const TRANSLATIONS: Record<AppLanguageCode, TranslationDictionary> = {
  en: { ...BASE_TRANSLATIONS.en, ...SHARED_UI_TRANSLATIONS.en, ...AUTH_TRANSLATIONS.en },
  hi: {
    ...BASE_TRANSLATIONS.hi,
    ...SHARED_UI_TRANSLATIONS.hi,
    ...AUTH_TRANSLATIONS.hi,
    ...HINDI_TRANSLATION_OVERRIDES,
    ...UI_LANGUAGE_OVERRIDES.hi,
  },
  ar: {
    ...BASE_TRANSLATIONS.ar,
    ...SHARED_UI_TRANSLATIONS.ar,
    ...AUTH_TRANSLATIONS.ar,
    ...UI_LANGUAGE_OVERRIDES.ar,
  },
};

@Injectable({
  providedIn: 'root',
})
export class LanguageService {
  private readonly storageKey = 'hrms_language_preference';

  readonly languages: AppLanguage[] = [
    { code: 'en', label: 'English', nativeLabel: 'English', locale: 'en-US', dir: 'ltr' },
    { code: 'hi', label: 'Hindi', nativeLabel: 'हिंदी', locale: 'hi-IN', dir: 'ltr' },
    { code: 'ar', label: 'Arabic', nativeLabel: 'العربية', locale: 'ar', dir: 'ltr' },
  ];

  readonly currentLanguage = signal<AppLanguage>(this.resolveInitialLanguage());

  constructor() {
    this.applyToDocument(this.currentLanguage());
  }

  setLanguage(code: AppLanguageCode): void {
    const next = this.languages.find((language) => language.code === code) ?? this.languages[0];
    this.currentLanguage.set(next);
    localStorage.setItem(this.storageKey, next.code);
    this.applyToDocument(next);
  }

  t(key: string, params?: Record<string, string | number | null | undefined>): string {
    const language = this.currentLanguage().code;
    const template = this.normalizeText(
      TRANSLATIONS[language]?.[key] ??
      TRANSLATIONS.en[key] ??
      key
    );

    if (!params) {
      return template;
    }

    return Object.entries(params).reduce((message, [paramKey, value]) => {
      return message.replaceAll(`{${paramKey}}`, String(value ?? ''));
    }, template);
  }

  private normalizeText(value: string): string {
    if (!this.looksBroken(value)) {
      return this.sanitizeText(value);
    }

    try {
      let current = value;

      for (let attempt = 0; attempt < 3; attempt += 1) {
        const decoded = new TextDecoder('utf-8').decode(
          Uint8Array.from(current, (char) => char.charCodeAt(0) & 0xff)
        );

        if (!decoded || decoded === current) {
          break;
        }

        current = decoded;

        if (!this.looksLikeMojibake(current)) {
          break;
        }
      }

      return this.sanitizeText(current);
    } catch {
      return this.sanitizeText(value);
    }
  }

  private looksLikeMojibake(value: string): boolean {
    return /(?:Ã.|Â.|à.|Ø.|Ù.|â.)/.test(value);
  }

  private looksBroken(value: string): boolean {
    return /[\u0000-\u0008\u000B\u000C\u000E-\u001F\u007F\uFFFD]|(?:Ã.|Â.|à.|Ø.|Ù.|â.)/.test(value);
  }

  private sanitizeText(value: string): string {
    return value
      .replace(/[\u0000-\u0008\u000B\u000C\u000E-\u001F\u007F\uFFFD]/g, '')
      .replace(/[ \t]{2,}/g, ' ')
      .replace(/\s+\n/g, '\n')
      .replace(/\n\s+/g, '\n')
      .normalize('NFC')
      .trim();
  }

  private resolveInitialLanguage(): AppLanguage {
    const stored = localStorage.getItem(this.storageKey) as AppLanguageCode | null;
    const fromStorage = this.languages.find((language) => language.code === stored);
    if (fromStorage) {
      return fromStorage;
    }

    const browserLanguage = (navigator.language || 'en').toLowerCase();
    if (browserLanguage.startsWith('hi')) {
      return this.languages.find((language) => language.code === 'hi') ?? this.languages[0];
    }
    if (browserLanguage.startsWith('ar')) {
      return this.languages.find((language) => language.code === 'ar') ?? this.languages[0];
    }

    return this.languages[0];
  }

  private applyToDocument(language: AppLanguage): void {
    document.documentElement.lang = language.code;
    document.documentElement.dir = language.dir;
  }
}
